#include<iostream>
using namespace std;

int main(){
    //integer
    int num=10;
    cout<<"Integer DataType:"   <<num   <<endl;

    //float
    float res=10.55;
    cout<<"Float DataType:"   <<res   <<endl;

    //double
    double data=10.554455;
    cout<<"Double DataType:"   <<data   <<endl;

    //boolean
    bool isTrue=true;
    cout<<"Boolean DataType:"   <<isTrue   <<endl;

    //char
    char ch='A';
    cout<<"Char DataType:"   <<ch   <<endl;

    //string
    string str="Hello world";
    cout<<"String DataType:"   <<str   <<endl;

    return 0;
}
